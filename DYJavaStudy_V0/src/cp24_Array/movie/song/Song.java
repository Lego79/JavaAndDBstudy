package cp24_Array.movie.song;


public class Song {
	

	String album;
	String singer;
	String title;
	String composer;
	String lyricist;
	String releaseDate;
	
	
	public Song(String album, String singer, String title, String composer, String lyricist, String releaseDate) {
		this.album = album;
		this.singer = singer;
		this.title = title;
		this.composer = composer;
		this.lyricist = lyricist;
		this.releaseDate = releaseDate;
	}
	

	
	
	


}
